package Tail;

/**
 *
 * @author David Barry <david.barry at crick.ac.uk>
 */
public class Monomer {

    private double x, y;
    public static final double DIAMETER = 7;

    public Monomer(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
    
}
